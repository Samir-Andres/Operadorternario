package vista;

public class Calculo {
	public 	String nombre;
	public int Num;
	public Calculo(String nombre, int num) {
		super();
		this.nombre = nombre;
		Num = num;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getNum() {
		return Num;
	}
	public void setNum(int num) {
		Num = num;
	}
	
	

}
